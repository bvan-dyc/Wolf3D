#include "libft.h"

t_list		*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list *new;
	t_list *sav;

	if (!lst)
		return (NULL);
	new = (*f)(lst);
	sav = new;
	while (lst->next)
	{
		new->next = (*f)(lst->next);
		new = new->next;
		lst = lst->next;
	}
	return (sav);
}
