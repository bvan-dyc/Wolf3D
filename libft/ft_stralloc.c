#include "libft.h"

char		*ft_stralloc(unsigned int n)
{
	char *str;

	str = NULL;
	if (n > 0)
		str = (char *)malloc(sizeof(char) * n);
	if (str == NULL)
		return (NULL);
	return (str);
}
