int		**ft_strintsplit(char *s, char c)
{
	int		**map;
	int		y;
	int		x;
	int		i;
	int		len;

	i = 0;
	y = 0;
	map = ft_matalloc(MAP_H, MAP_W);
	len = ft_strlen(s);
	while (s[i] && i < len)
	{
		x = 0;
		while (s[i] && i < len && x < MAP_W)
		{
			if (s[i] != c)
			{
				map[y][x] = s[i] - '0';
				x++;
			}
			i++;
		}
		y++;
	}
	return (map);
}

void	printtab(int **map)
{
	int x;
	int y;

	y = 0;
	while (y < MAP_H)
	{
		x = 0;
		while (x < MAP_W)
		{
			ft_putnbr(map[y][x]);
			x++;
			if (x < MAP_W)
				ft_putchar(',');
		}
		ft_putchar('\n');
		y++;
	}
}

